package setterinjection;

public class demo {
private int x;
private String y;
public int getX() {
	return x;
}
public void setX(int x) {
	this.x = x;
}
public String getY() {
	return y;
}
public void setY(String y) {
	this.y = y;
}
public void show() {
	System.out.println("x:"+100);
	System.out.println("y:"+"pradeep");
}
}
