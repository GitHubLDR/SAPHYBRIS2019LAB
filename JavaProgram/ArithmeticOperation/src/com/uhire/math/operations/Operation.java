package com.uhire.math.operations;

public class Operation {

	public void add(int i, int j) {
		int res = i + j;
		System.out.println("i + j = " + res);
		
	}

	public void sub(int i, int j) {
		int res = i + j;
		System.out.println("i - j = " + res);
		
	}

	public void div(int i, int j) {
		int res = i / j;
		System.out.println("i / j = " + res);
		
	}

	public void mul(int i, int j) {
		int res = i * j;
		System.out.println("i * j = " +res);
		
	}

}
