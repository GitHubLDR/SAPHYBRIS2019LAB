package com.uhire.arthoperations;

public class Arithmetic
{
	static int x,y,z;
	
	public void Add(int x,int y)
	{
		z = x + y;
		System.out.println(z);
	}
    
	public void Sub(int x,int y)
	{
		z = x - y;
		System.out.println(z);
	}

	public void Mul(int x,int y)
	{
		z = x * y;
		System.out.println(z);
	}

	public void Div(int x,int y)
	{
		z = x / y;
		System.out.println(z);
	}

}
